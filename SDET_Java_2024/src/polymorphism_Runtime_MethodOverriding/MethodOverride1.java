package polymorphism_Runtime_MethodOverriding;

/* This is runtime polymorphism
   Method name is same and parameters are also same, 
   but the these methods are in different classes
   
   The method in parent class, the same method will be 
   in child class also with same parameters.
*/

public class MethodOverride1 {

	public void eat() {
		System.out.println("I am eating");
	}
}
